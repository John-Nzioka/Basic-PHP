<?php

/* Control Structures (if / else / elseif / else if) */

$score = 89;

if ($score>=90) {
    echo 'A';
}elseif ($score>=80) {
    echo 'B';
}elseif ($score>=70) {
    echo 'C';
}elseif ($score>=60) {
    echo 'D';
}elseif ($score>=50) {
    echo 'E';
}elseif ($score>=40) {
    echo 'F'  ;
}else {
    echo 'Out of Range';
}  
